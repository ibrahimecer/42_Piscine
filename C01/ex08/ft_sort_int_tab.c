void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	temp;
	int	flag;

	temp = 0;
	flag = 1;
	while (flag)
	{
		i = 0;
		flag = 0;
		while (i < size -1)
		{
			if (tab[i] > tab[i + 1])
			{
				temp = tab[i];
				tab[i] = tab[i + 1];
				tab[i + 1] = temp;
				flag = 1;
			}
			++i;
		}
	}
}
